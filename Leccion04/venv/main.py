#Ejercicio con While
#Imprimir los valores del 0 al 5
contador = 0
maximo = 5
while contador <= maximo:
    print(contador)
    contador += 1 #la variable de contador la estamos aumentando en 1: contador = contador + 1
else:
    print("Fin ciclo while")